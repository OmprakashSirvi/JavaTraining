package ques2;

public class Test {
	private int hello;
	private int notHello;
	private int what;

	public Test(int hello, int notHello, int what) {
		super();
		this.hello = hello;
		this.notHello = notHello;
		this.what = what;
	}

	public int getHello() {
		return hello;
	}

	public void setHello(int hello) {
		this.hello = hello;
	}

	public int getNotHello() {
		return notHello;
	}

	public void setNotHello(int notHello) {
		this.notHello = notHello;
	}

	public int getWhat() {
		return what;
	}

	public void setWhat(int what) {
		this.what = what;
	}
}
